using System;

class Program
{
    static void Main()
    {
        string s = Console.ReadLine();
        string[] a = s.Split(new char[] { ' ' });
        s = "";
        for (int i = 0; i < a.Length; i++)
        {
            if ((a[i].Length == 2 && Char.IsPunctuation(a[i][1])))
            {
                continue;
            }
            int cnt = 0;
            if (!Char.IsPunctuation(a[i][cnt]))
            {
                cnt++;
                while (cnt < a[i].Length && Char.IsPunctuation(a[i][cnt]))
                {
                    cnt++;
                }
                if (cnt == a[i].Length)
                {
                    continue;
                }
            }
            s += a[i] + ' ';
        }
        Console.WriteLine(s);
        Console.ReadKey();

    }
}