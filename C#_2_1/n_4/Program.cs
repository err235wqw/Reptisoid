using System;

class Program
{
    static void Main()
    {
        Console.Write("Введите N = ");
        int n = Convert.ToInt32(Console.ReadLine());
        Console.Write("Введите C = ");
        int c = Convert.ToInt32(Console.ReadLine());
        int temp = c;
        while (temp < n && c != 1)
        {
            temp *= c;
        }
        string result = (n == temp || n == 1) ? "Является" : "Не является";
        Console.Write(result);
    }
}