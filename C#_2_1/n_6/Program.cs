using System;

class Program
{
    static int duuu(ref string n, int b, int q)
    {
        if (b == 0)
        {
            if (n[0] == '1')
            {
                return q;
            }
            return 0;
        }
        q *= 2;
        if (n[b] == '1')
        {
            return duuu(ref n, b - 1, q) + q / 2;
        }
        return duuu(ref n, b - 1, q);
    }
    static void Main()
    {
        Console.Write("Введите N = ");
        string n = Console.ReadLine();
        int result;
        result = duuu(ref n, n.Length - 1, 1);
        Console.Write($"{result}");
    }
}