using System;

class Program
{
    static void Main()
    {
        Console.Write("Введите N = ");
        int n = Convert.ToInt32(Console.ReadLine());
        int[,] a = new int[n, n];
        Random random = new Random();
        for (int i = 0; i < n; i++)
        {
            for (int j = 0; j < n; j++)
            {
                a[i, j] = Convert.ToInt32(Console.ReadLine());
            }
        }
        int[] answ = new int[n];
        for (int j = 0; j < n; j++)
        {
            int sum = 0;
            for (int i = 0; i < n; i++)
            {
                sum += Math.Min(0, a[i, j]);
            }
            answ[j] = sum;
        }
        for (int i = 0; i < n; i++)
        {
            Console.WriteLine($"{i} столбец: {answ[i]}");
        }

    }
}